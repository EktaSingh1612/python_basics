'''
OPERATORS : Python has diff types of operators for diff operations. To create a calculator we require arithmetic operators.

+ --> addition
- --> subtraction
* --> multplication
/ --> division
% --> modulus i.e., remainder
// --> floor division
** --> exponential
'''

print(15 + 6)
print(15 - 6)
print(15 * 6)
print(15 / 6)
print(15 % 6)
print(15 // 6)
print(15 ** 6)

